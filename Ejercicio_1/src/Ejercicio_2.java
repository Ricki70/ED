
public class Ejercicio_2 {

	public static void main(String[] args) {

		int N = 10;
		System.out.println ("N = " + N);
		
		N += 77;
		System.out.println ("\nN + 77 = " + N);
		
		N -= 3;
		System.out.println ("Dado resultado - 3 = " + N);
		
		N *= 2;
		System.out.println ("Dado resultado * 2 = " + N);
		
	}

}
